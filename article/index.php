				<p>Hier soir, Odile réunissait chez Google Launchpad la communauté @beautyTechSF pour son 7ème event à SF (déjà !): une soirée à écouter et échanger avec 5 fondateurs inspirants dans le domaine de la beauté et de la mode, partageant leur histoire et leur parcours en tant qu’entrepreneur.</p>
				<p>FAB est une découverte pour vous? Tout est décrit sur le site de la communauté et il y se pourrait bien qu’un évènement soit organisé prochainement «près de chez vous».</p>
				<span>"Dans ce 1er volet, je vous invite à connaître les entrepreneurs invités. Le 2ème volet vous livrera l’essentiel à retenir de cette soirée d’hier. Assurez-vous de le recevoir en vous inscrivant à ma News."</span>
				<p>Pas très éloigné du modèle de Feelunique (Joël Palix, son CEO à l’époque, est venu en déc dernier à San Francisco expliquer à la communauté FAB le modèle), IPSY s’est créé en 2011 autour d’une communauté, aidé en cela par l’influenceuse Michelle Phan et sa capacité à identifier et engager les key influencers et engager ses propres followers.</p>
			
